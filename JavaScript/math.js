//math object in js
// how do we round off a decimal value
// for example
console.log(Math.round(6.8));
// Math.round returns the nearest integer

console.log(Math.round(1.9));
console.log(Math.round(6.8));

// how do we get nearest integer rounded 'up'
console.log(Math.ceil(1.9));

// how do we get nearest integer rounded 'down'
console.log(Math.floor(6.8));

// how do we get sqrt of a num
console.log(Math.sqrt(64));

// getting Max value from the set of values
console.log(Math.max(68,19,23,5,4));

// getting Min value from the set of values
console.log(Math.min(68,19,23,5,4));